from langchain_core.prompts import PromptTemplate


def main() -> None:
    support_prompt = PromptTemplate.from_template(
        """
        Ты - вежливый и профессиональный специалист службы поддержки.
        
        Контекст:
        - Имя пользователя: {user_name}
        - Продукт: {product_name}
        - Проблема: {issue_description}
        - Тон ответа: {tone}
        
        Задача:
        Сформируй ответ пользователю, в котором:
        1. есть краткое приветствие по имени
        2. подтверждается, что проблема понята
        3. дается одно возможное объяснение причины
        4. предлагаются 2-3 шага для решения
        5. в конце есть вежливое завершение
        
        Ограничения:
        - пиши на русском языке
        - не придумывай недоступные функции продукта
        - стиль должен соответствовать параметру tone
        - ответ должен быть не длиннее 180 слов
        
        Формат ответа:
        Готовое сообщение пользователю без дополнительных комментариев.
        """.strip()
    )

    examples = [
        {
            "user_name": "Анна",
            "product_name": "мобильное приложение банка",
            "issue_description": "не приходит SMS-код для входа в аккаунт",
            "tone": "спокойный и заботливый",
        },
        {
            "user_name": "Игорь",
            "product_name": "онлайн-магазин электроники",
            "issue_description": "заказ оформлен, но статус доставки не обновляется 3 дня",
            "tone": "деловой и уверенный",
        },
        {
            "user_name": "Мария",
            "product_name": "образовательная платформа",
            "issue_description": "после оплаты курс не открылся в личном кабинете",
            "tone": "дружелюбный и обнадеживающий",
        },
    ]

    for index, example in enumerate(examples, start=1):
        formatted_prompt = support_prompt.format(**example)
        print(f"\n{'=' * 80}")
        print(f"Пример {index}")
        print(f"{'=' * 80}")
        print(formatted_prompt)


if __name__ == "__main__":
    main()